async function getOmdbRatings(imdbId: string) {
  if (!imdbId) return { imdbRating: null, imdbVotes: null };
  try {
    const response = await fetch(`http://www.omdbapi.com/?i=${imdbId}&apikey=${OMDB_API_KEY}`);
    const data = await response.json();
    if (data.Response === "True") {
      return {
        imdbRating: parseFloat(data.imdbRating) || null,
        imdbVotes: parseInt(data.imdbVotes?.replace(/,/g, "")) || null,
      };
    }
  } catch (e) {}
  return { imdbRating: null, imdbVotes: null };
}

function parseMovieEntry(entry: string): { title: string; year?: number } {
  const match = entry.match(/^(.+?)\s*\((\d{4})\)$/);
  if (match) {
    return { title: match[1].trim(), year: parseInt(match[2]) };
  }
  return { title: entry.trim() };
}

async function importMovie(entry: string) {
  const { title, year } = parseMovieEntry(entry);
  
  // Check if already exists by title (case-insensitive for SQLite using LOWER)
  const existing = await prisma.movie.findFirst({
    where: { title: { contains: title } }
  });
  
  if (existing && existing.title.toLowerCase() === title.toLowerCase()) {
    console.log(`â­ï¸  Skipping "${title}" (already exists)`);
    return null;
  }

  // Search TMDB
  const searchResult = await searchTMDB(title, year);
  if (!searchResult) {
    console.log(`âŒ Not found on TMDB: "${title}"`);
    return null;
  }

  // Check if tmdbId already exists
  const existingByTmdb = await prisma.movie.findUnique({
    where: { tmdbId: searchResult.id }
  });
  
  if (existingByTmdb) {
    console.log(`â­ï¸  Skipping "${title}" (TMDB ID ${searchResult.id} exists as "${existingByTmdb.title}")`);
    return null;
  }

  // Get full details
  const [details, credits, videos] = await Promise.all([
    getMovieDetails(searchResult.id),
    getMovieCredits(searchResult.id),
    getMovieVideos(searchResult.id),
  ]);

  // Get trailer
  const trailer = videos.results?.find(
    (v: any) => v.type === 'Trailer' && v.site === 'YouTube'
  );

  // Get director and cast
  const director = credits.crew?.find((c: any) => c.job === 'Director')?.name || null;
  const topCast = credits.cast?.slice(0, 2).map((c: any) => ({
    name: c.name,
    character: c.character,
    profilePath: c.profile_path,
  })) || [];

  // Get IMDB ratings
  const omdb = await getOmdbRatings(details.imdb_id);

  // Create movie
  const movie = await prisma.movie.create({
    data: {
      title: details.title,
      year: details.release_date ? parseInt(details.release_date.split('-')[0]) : null,
      description: details.overview,
      tagline: details.tagline || null,
      runtime: details.runtime || null,
      tmdbId: details.id,
      imdbId: details.imdb_id || null,
      posterPath: details.poster_path,
      backdropPath: details.backdrop_path,
      trailerKey: trailer?.key || null,
      genres: details.genres?.map((g: any) => g.name).join(',') || null,
      tmdbRating: details.vote_average,
      tmdbVotes: details.vote_count,
      imdbRating: omdb.imdbRating,
      imdbVotes: omdb.imdbVotes,
      director,
      cast: JSON.stringify(topCast),
    },
  });

  console.log(`âœ… Imported: ${movie.title} (${movie.year})`);
  return movie;
}

async function main() {
  console.log(`ðŸŽ¬ Starting import of ${moviesToImport.length} movies...\n`);
  
  let imported = 0;
  let skipped = 0;
  let failed = 0;

  for (let i = 0; i < moviesToImport.length; i++) {
    const entry = moviesToImport[i];
    console.log(`[${i + 1}/${moviesToImport.length}] Processing: ${entry}`);
    
    try {
      const result = await importMovie(entry);
      if (result) imported++;
      else skipped++;
    } catch (error) {
      console.log(`âŒ Error importing "${entry}":`, error);
      failed++;
    }

    // Rate limiting - wait 250ms between requests
    await new Promise(resolve => setTimeout(resolve, 250));
  }

  console.log(`\nâœ… Import complete!`);
  console.log(`   Imported: ${imported}`);
  console.log(`   Skipped: ${skipped}`);
  console.log(`   Failed: ${failed}`);
  
  const total = await prisma.movie.count();
  console.log(`\nðŸ“Š Total movies in database: ${total}`);
}

